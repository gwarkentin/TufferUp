<script>
import PostPreview from '../components/PostPreview.vue'
import axios from 'axios'

export default {
  data() {
    return {
      'posts': Object,
      'category': String,
    }
  },
  components: {
    PostPreview
  },
  watch:{
    $route (newroute, oldroute){
        this.category = newroute.params.category;
        this.tryAjax();
    }
  },
  mounted() {
    this.tryAjax();
  },
  methods: {
    tryAjax() {
        axios.get('http://localhost:3001/api/category/' + this.category)
        .then(response => {
          const rd = response.data;
          console.log(rd)
          this.posts = rd.documents
          this.error = response.data.error
          this.ogdata = false
        })
        .catch(function (error) {
          this.error = error;
        })
        .then(function () {
          // always executed
      });  
    }
  }
}
</script>

