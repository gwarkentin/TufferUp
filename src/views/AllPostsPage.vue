<template>
  <div class="container">
  <div v-if="haserror" class="alert alert-danger" role="alert">
    <p>{{ error }}</p>
  </div>
    <!-- generated by for loop from response from db -->
    <div class="row">
      <div v-for="post in posts" :key="post" class="col-sm px-4">
        <PostPreview :title="post.title" :price="post.price" :postID="post._id" :imgs="post.imgs" />
      </div>
    </div>
</div>
</template>

